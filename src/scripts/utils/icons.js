/**
 * Render an icon with specified dimensions and class
 * @param {string} name - Icon name from Icons object
 * @param {object} options - Icon options
 * @returns {string} SVG string
 */
export function renderIcon(name, options = {}) {
  const {
    width = 16,
    height = 16,
    className = '',
    ariaLabel = '',
  } = options;

  const iconSvg = Icons[name];
  if (!iconSvg) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }

  // Add attributes to SVG
  return iconSvg
    .trim()
    .replace('<svg', `<svg width="${width}" height="${height}" class="icon ${className}" ${ariaLabel ? `aria-label="${ariaLabel}"` : 'aria-hidden="true"'}`);
}

/**
 * Create an icon element (returns DOM node)
 */
export function createIcon(name, options = {}) {
  const template = document.createElement('template');
  template.innerHTML = renderIcon(name, options).trim();
  return template.content.firstChild;
}
